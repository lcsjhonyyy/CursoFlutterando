void main() {
  // Variável inteira (int)
  int age = 25;
  print('age: $age');

  // Variável de ponto flutuante (double)
  double reais = 1.75;
  print('reais: $reais');

  // Variável booleana (bool)
  bool isEstudante = true;
  print('É estudante: $isEstudante');

  // Variável de string (String)
  String nome = 'Lucas';
  print('Nome: $nome');

  // Lista (List)
  List<String> frutas = ['Maçã', 'Banana', 'Laranja'];
  print('Frutas: $frutas');

  // Mapa (Map)
  Map<String, int> notas = {
    'Matemática': 95,
    'Português': 88,
    'História': 76,
  };
  print('Notas: $notas');

  // Conjunto (Set)
  Set<String> cores = {'vermelho', 'verde', 'azul'};
  print('Cores: $cores');
}
